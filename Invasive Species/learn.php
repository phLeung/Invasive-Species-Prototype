        <h2 style="font-family:Coustard;font-size:34px;">What's an invasive species?</h2>
        <br>
        <p>An invasive is a plant, animal, or insect which is not naturally 
            found in an area, and which spreads and harms human health, native plants, and native animals. By some estimates, 
            there are over 5,000 invasive plants and over 2,300 exotic animals spreading throughout the United States, but most 
            of these species are not doing any noticable harm. The problem is that some invasive species do not find any natural 
            predators in their new home to control their population, so they spread easily and quickly.
        </p>
        <div class="mx-auto card" style="background-color: #f1f1f1">
            <div style="text-align:center">
            <h6 style="font-weight: bold; font-size:20px;">
                Of every 100 foreign animals and plants brought to the United States</h6>
                 -About 10 will start to spread through the country <br>
                 -5 will move into our native forests, fields, and wetlands <br>
                 -2-3 will become pests, causing problems and costing a lot of money to slow down or stop
                 </div>
        </div>
        <p>Some invasives have made native species become rare, and they eat the rich varieties of native plants, making the 
            natural landscapes (hills and valleys for example) look boring and similar to each other. It also costs a lot of 
            money to try to control and stop foreign pests from invading and spreading, and occasionally their populations still 
            explodes.  Zebra mussels, for example, began to spread in through our rivers and lakes around 1985. By 2003, 
            the cost of controlling the mussels was over $5 billion dollars. The nationwide cost to taxpayers for controlling 
            introduced plants, animals and insects is now over $137 billion dollars a year.            
        </p>    
        <p>Because of New York State’s unique location, many invasive species start here and spread. New plants and animals 
        have been coming here since the first explorers came ashore in the 15th century. Today, one in three plants in New York 
        State have come from somewhere else. Many foregin species have been around so long, we take them for granted. Today our 
        airports and harbors in the Hudson and Mohawk River valleys still spread invasive species.
        </p>
        <h4 style="font-family:Coustard">How do they get here?</h4>
        <p>Transportation - Whenever people, products, or animals and plants move, a critter might somehow be brought along. They can be hiding 
         in airplanes, automobiles, trucks, boats, cargo ships, trains, or even on your shoes. In the past, people immigrating 
         or traveling often brought plants and animals they liked with them for food, medicine, for their beauty, or simply to 
         help them feel more at home in a new land. Today, invasives usually get started by accident or as a result of world trade.
        </p>
        <h4 style="font-family:Coustard">Are exotic introductions still occurring?</h4>
        <p>Yes, and more and more often because of increasing world trade, especially with places where climates are similar to our own. 
        Look in stores - you’ll find labels from all over the world (including countries like China, India,El Salvador, Mexico, and Eastern Europe).
        Look at the clothes, toys, foods, furniture, lumber, living fish and animals, landscape plants in stores, and the tourists all around you.
        Have you ever traveled? Everytime someone travels or when something is sent in the mail, there is a small chance an unwanted hitchhiker 
        has come along.
        </p>
